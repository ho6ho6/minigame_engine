/*ファイルやフォルダに関して*/

#ifndef _UTIL
#define _UTIL

#pragma once
#include <filesystem>
#include <Windows.h>


namespace fs = std::filesystem;

fs::path GetAssetDir() {
    char buf[MAX_PATH];
    GetModuleFileNameA(nullptr, buf, MAX_PATH);
    fs::path exe = buf;
    return exe.remove_filename() / "Assets";
}

#endif // !_UTIL

